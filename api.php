<h1>¿Qué es una API?</h1>
        <p>Una API, o Interfaz de Programación de Aplicaciones, es un conjunto de reglas y protocolos que permite que diferentes aplicaciones o sistemas informáticos se comuniquen entre sí. En otras palabras, una API actúa como un intermediario que permite que dos programas se entiendan y se comuniquen, permitiendo el intercambio de datos y funcionalidades.</p>
        
        <h4>Características clave de una API:</h2>
        <ul>
            <li><b>Comunicación:</b> Permite que una aplicación solicite información o realice acciones en otra aplicación o sistema.</li>
            <li><b>Estándares:</b> Las API siguen estándares y reglas específicas que definen cómo deben estructurarse las solicitudes y respuestas.</li>
            <li><b>Reutilización de código:</b> Las API permiten a los desarrolladores reutilizar funcionalidades de otras aplicaciones sin necesidad de conocer todos los detalles internos de esas aplicaciones.</li>
            <li><b>Abstracción:</b> Las API proporcionan una capa de abstracción, lo que significa que los usuarios de la API no necesitan conocer los detalles internos de cómo funciona la aplicación o sistema subyacente.</li>
        </ul>
        
        <p>Las API son fundamentales en el mundo de la programación y se utilizan ampliamente en el desarrollo de aplicaciones web y móviles. Permiten la integración de servicios, la automatización de tareas y la expansión de funcionalidades.</p>
